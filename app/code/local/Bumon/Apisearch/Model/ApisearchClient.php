use Exception;
/**
 * Class ApisearchClient
 */
class Bumon_Apisearch_Model_ApisearchClient
{
    /**
     * @var array
     *
     * Elements to update
     */
    private $elementstoPut;

    /**
     * @var array
     *
     * Elements to delete
     */
    private $elementsToDelete;

    /**
     * @var string
     *
     * Host
     */
    private $host;

    /**
     * @var string
     *
     * Version
     */
    private $version;

    /**
     * @var string
     *
     * App id
     */
    private $appUUID;

    /**
     * @var string
     *
     * Index id
     */
    private $indexUUID;

    /**
     * @var string
     *
     * Token
     */
    private $tokenUUID;

    /**
     * Set credentials.
     *
     * @param string $appUUID
     * @param string $indexUUID
     * @param string $tokenUUID
     */
    public function setCredentials(
        $appUUID,
        $indexUUID,
        $tokenUUID
    ) {
        Mage::log("setCredentials [START]", null, 'apisearch.log', true);
        $this->appUUID = $appUUID;
        $this->indexUUID = $indexUUID;
        $this->tokenUUID = $tokenUUID;
        Mage::log("setCredentials [END]", null, 'apisearch.log', true);
    }

    /**
     * Repository constructor.
     *
     * @param string $host
     * @param string $version
     */
    
    public function __construct(
        $host,
        $version
    ) {
        Mage::log("__construct [START]", null, 'apisearch.log', true);
        Mage::log("host >> ".$host, null, 'apisearch.log', true);
        Mage::log("version >> ".$version, null, 'apisearch.log', true);

        $this->resetCachedElements();
        $this->host = $host;
        $this->version = $version;

        Mage::log("__construct [END]", null, 'apisearch.log', true);
    }

    /**
     * Reset cache.
     */
    private function resetCachedElements()
    {
        $this->elementstoPut = [];
        $this->elementsToDelete = [];
    }

    /**
     * Reset index.
     *
     * @throws Exception
     */
    public function resetIndex()
    {
        Mage::log("resetIndex [START]", null, 'apisearch.log', true);

        $this->get(
            '/'.$this->appUUID.'/indices/'.$this->indexUUID.'/reset',
            'POST'
        );

        Mage::log("resetIndex [END]", null, 'apisearch.log', true);
    }

    /**
     * Put item.
     *
     * @param array $item
     */
    public function putItem(array $item)
    {
        Mage::log("putItem [START]", null, 'apisearch.log', true);
        Mage::log($item, null, 'apisearch.log', true);

        $itemUUID = $this->composeUUID($item['uuid']);

        Mage::log("uuid >> ", null, 'apisearch.log', true);
        Mage::log($item['uuid'], null, 'apisearch.log', true);

        $this->elementstoPut[$itemUUID] = $item;
        unset($this->elementsToDelete[$itemUUID]);

        Mage::log("putItem [END]", null, 'apisearch.log', true);
    }

    /**
     * Generate item documents.
     *
     * @param array[] $items
     */
    public function putItems(array $items)
    {
        Mage::log("putItems [START]", null, 'apisearch.log', true);

        foreach ($items as $item) {
            $this->putItem($item);
        }

        Mage::log("putItems [END]", null, 'apisearch.log', true);
    }

    /**
     * Delete item documents by uuid.
     *
     * @param array[] $uuids
     */
    public function deleteItems(array $uuids)
    {
        Mage::log("deleteItems [START]", null, 'apisearch.log', true);

        foreach ($uuids as $uuid) {
            $this->deleteItem($uuid);
        }

        Mage::log("deleteItems [END]", null, 'apisearch.log', true);
    }

    /**
     * Delete item document by uuid.
     *
     * @param array $uuid
     */
    public function deleteItem(array $uuid)
    {
        Mage::log("deleteItem [START]", null, 'apisearch.log', true);
        Mage::log("uuid >> ".$uuid, null, 'apisearch.log', true);

        $itemUUID = $this->composeUUID($uuid);
        $this->elementsToDelete[$itemUUID] = $uuid;
        unset($this->elementstoPut[$itemUUID]);
        
        Mage::log("deleteItem [END]", null, 'apisearch.log', true);
    }

    /**
     * Flush all.
     *
     * This flush can be avoided if not enough items have been generated by
     * setting $skipIfLess = true
     *
     * @param int  $bulkNumber
     * @param bool $skipIfLess
     *
     * @throws Exception
     */
    public function flush(
        int $bulkNumber = 100,
        bool $skipIfLess = true
    ) {
        Mage::log("flush [START]", null, 'apisearch.log', true);

        if (
            $skipIfLess &&
            count($this->elementstoPut) < $bulkNumber
        ) {
            return;
        }

        $offset = 0;

        try {
            while (true) {
                $items = array_slice(
                    $this->elementstoPut,
                    $offset,
                    $bulkNumber
                );

                if (empty($items)) {
                    break;
                }

                $this->flushItems($items, []);
                $offset += $bulkNumber;
            }

            $this->flushItems([], $this->elementsToDelete);
        } catch (Exception $exception) {
            /*
             * No matter the exception is thrown, cached elements should be
             * deleted
             */
            $this->resetCachedElements();

            throw $exception;
        }

        Mage::log("flush [END]", null, 'apisearch.log', true);

        $this->resetCachedElements();
    }

    /**
     * Flush items.
     *
     * @param array[] $itemstoPut
     * @param array[] $itemsToDelete
     *
     * @throws Exception
     */
    private function flushItems(
        array $itemstoPut,
        array $itemsToDelete
    ) {

        if (!empty($itemstoPut)) {
            $this
                ->get(
                    '/'.$this->appUUID.'/indices/'.$this->indexUUID.'/items',
                    'put',
                    [],
                    array_values($itemstoPut)
                );
        }

        if (!empty($itemsToDelete)) {
            $this
                ->get(
                    '/'.$this->appUUID.'/indices/'.$this->indexUUID.'/items',
                    'delete',
                    [],
                    array_values($itemsToDelete)
                );
        }
    }

    /**
     * Compose unique id.
     *
     * @param array $itemUUID
     *
     * @return string
     */
    private function composeUUID(array $itemUUID)
    {
        Mage::log("composeUUID [START]", null, 'apisearch.log', true);
        Mage::log($itemUUID['id'].'~'.$itemUUID['type'], null, 'apisearch.log', true);

        return $itemUUID['id'].'~'.$itemUUID['type'];

        Mage::log("composeUUID [END]", null, 'apisearch.log', true);
    }

    /**
     * Make a request and return response.
     *
     * @param string $endpoint
     * @param string $method
     * @param array  $parameters
     * @param array  $body
     *
     * @return string
     *
     * @throws Exception
     */
    private function get(
        $endpoint,
        $method,
        array $parameters = [],
        array $body = []
    ) {
        Mage::log("get [START]", null, 'apisearch.log', true);

        $endpoint = str_replace([
            '{{app_uuid}}',
            '{{index_uuid}}',
            '{{token_uuid}}',
        ], [
            $this->appUUID,
            $this->indexUUID,
            $this->tokenUUID,
        ], $endpoint);

        Mage::log("endpoint >>> ", null, 'apisearch.log', true);
        Mage::log($endpoint, null, 'apisearch.log', true);
        Mage::log(" <<< ", null, 'apisearch.log', true);

        $url = sprintf('%s/%s/%s?token=%s',
            rtrim($this->host, '/'),
            trim($this->version, '/'),
            ltrim($endpoint, '/'),
            $this->tokenUUID
        );

        $parameters = array_map('urlencode', $parameters);
        foreach ($parameters as $parameterKey => $parameterValue) {
            $url .= "&$parameterKey=$parameterValue";
        }

        if ('get' !== $method) {
            $data = json_encode($body);
            $context = stream_context_create([
                'http' => [
                    'method' => $method,
                    'ignore_errors' => true,
                    'header' => "Content-type: application/json\r\n".
                        "Accept: application/json\r\n".
                        "Connection: close\r\n".
                        'Content-length: '.strlen($data)."\r\n",
                    'content' => $data,
                ],
            ]);

            $data = file_get_contents($url, false, $context);
        } else {
            $data = file_get_contents($url);
        }

        $code = $this->parseResponseStatusCode($http_response_header['0']);

        if ('2' !== substr($code, 0, 1)) {
            throw new Exception($data, $code);
        }

        return json_decode($data, true);
        
        Mage::log("get [END]", null, 'apisearch.log', true);
    }

    /**
     * Parse response header and return value.
     *
     * @param string $header
     *
     * @return int
     */
    private function parseResponseStatusCode($header)
    {
        Mage::log("parseResponseStatusCode [START] ", null, 'apisearch.log', true);
        try {
            list(, $code, $status) = explode(' ', $header, 3);

            Mage::log("code >> ".$code, null, 'apisearch.log', true);

            return (int) $code;
        } catch (Exception $exception) {
            // Silent pass
            Mage::log("exception >> ", null, 'apisearch.log', true);
            Mage::log(json_encode($exception), null, 'apisearch.log', true);
        }

        Mage::log("parseResponseStatusCode [END] ", null, 'apisearch.log', true);
        return null;
    }
}